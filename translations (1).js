document.addEventListener('DOMContentLoaded', () => {
    const langDropdown = document.getElementById('langDropdown');
    const currentLangText = document.getElementById('currentLangText');
    
    // Toggle menu
    if (langDropdown) {
        const selectedBox = langDropdown.querySelector('.dropdown-selected');
        const options = langDropdown.querySelectorAll('.dropdown-options li');
        
        selectedBox.addEventListener('click', (e) => {
            langDropdown.classList.toggle('open');
            e.stopPropagation();
        });
        
        // Click outside closes it
        document.addEventListener('click', () => {
            langDropdown.classList.remove('open');
        });
        
        // Option clicked
        options.forEach(option => {
            option.addEventListener('click', () => {
                const val = option.getAttribute('data-value');
                setLanguage(val);
            });
        });
    }
    
    function setLanguage(lang) {
        if (!translations[lang]) return;
        document.documentElement.lang = lang;
        if (lang === 'ar') {
            document.body.style.direction = 'rtl';
        } else {
            document.body.style.direction = 'ltr';
        }
        
        if (currentLangText) {
            currentLangText.textContent = lang.toUpperCase();
        }

        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                if (el.tagName.toLowerCase() === 'input' || el.tagName.toLowerCase() === 'textarea') {
                    el.placeholder = translations[lang][key];
                } else if (el.tagName.toLowerCase() === 'option') {
                    el.textContent = translations[lang][key];
                } else if (el.tagName.toLowerCase() === 'title') {
                    document.title = translations[lang][key];
                } else {
                    el.innerHTML = translations[lang][key];
                }
            }
        });
        localStorage.setItem('preferredLang', lang);
    }
    
    const savedLang = localStorage.getItem('preferredLang') || 'tr';
    setLanguage(savedLang);
});
